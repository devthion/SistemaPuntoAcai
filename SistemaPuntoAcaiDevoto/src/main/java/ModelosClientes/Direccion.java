package ModelosClientes;

public class Direccion {
	
	private String calle;
	private int numero;
	private String barrio;
	private int codPostal;
	
	public Direccion(String calle, int numero, String barrio, int codPostal) {
		this.calle = calle;
		this.numero = numero;
		this.barrio = barrio;
		this.codPostal = codPostal;
	}
	
	
}
